public class car {

	private String model;
	private String renk;
	private int kapı;
	private int hız;

	public void setModel(String model) {
		this.model = model;

	}

	public String getModel() {
		return this.model;
	}

	public String getRenk() {
		return renk;
	}

	public void setRenk(String renk) {
		this.renk = renk;
	}

	public int getKapı() {
		return kapı;
	}

	public void setKapı(int kapı) {
		this.kapı = kapı;
	}

	public int getHız() {
		return hız;
	}

	public void setHız(int hız) {
		this.hız = hız;
	}

}
