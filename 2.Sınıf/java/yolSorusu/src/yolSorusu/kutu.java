package yolSorusu;

public class kutu {

	private int deger;
	private boolean tahmin=true;

	public kutu(int deger) {
		this.deger = deger;
	}

	public int getDeger() {
		return deger;
	}

	public void setDeger(int deger) {
		this.deger = deger;
	}

	public boolean isTahmin() {
		return tahmin;
	}

	public void setTahmin(boolean tahmin) {
		this.tahmin = tahmin;
	}

}
